# Faça um programa que leia um número Inteiro e mostre na tela o seu sucessor e seu antecessor.

# variavel que recebe um inteiro a ser realizar o processo de antecessor e sucessor no print a seguir
n1 = int(input('Digite um número: '))

# imprime o valor informado -1 e +1, conforme valor inserido no 'n1'
print(f'O antecessor de {n1} é {n1 - 1} e o sucessor é {n1 + 1}')